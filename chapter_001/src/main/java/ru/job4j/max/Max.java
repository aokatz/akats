package ru.job4j.max;

/**
 * @author AKats
 */
public class Max {
    /**
     * Return maximum out of first and second
     * @param first - first
     * @param second - second
     * @return - maximum out of first and second
     */
    public int max(int first, int second) {
        return first > second ? first : second;
    }
}
